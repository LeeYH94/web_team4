package hotels;

public class HotelModel {	
	private String hotel_num;
	private String hotel_name;
	private String location;
	private String room_total;
	
	public String getHotel_num() {
		return hotel_num;
	}
	public void setHotel_num(String hotel_num) {
		this.hotel_num = hotel_num;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getRoom_total() {
		return room_total;
	}
	public void setRoom_total(String room_total) {
		this.room_total = room_total;
	}
	
	
}
